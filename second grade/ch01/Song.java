class Song
{
	String title;
	String artist;
	String album;
	String composer;
	int year;
	int track_num;
	public Song(String title,String artist,String album,String composer,int year,int track_num){
		this.title=title;
		this.artist=artist;
		this.album=album;
		this.composer=composer;
		this.year=year;
		this.track_num=track_num;
	}

	
}
